    public function actionEditableUpdate($id)
    {
        $model = $this->findModel($id);

        $posted = Yii::$app->request->post();
        $post = ['TunjanganInstansiJenisJabatanKelas' => $posted];

        if ($model->load($post)) {
            if ($model->save()) {
                return Json::encode(['output' => '', 'message' => '']);
            } else {
                return Json::encode(['output' => '', 'message' => 'error 500, internal server error']);
            }
        } else {
            return Json::encode(['output' => '', 'message' => 'error 500']);
        }
    }

    public function exportExcel($params)
    {
        $PHPExcel = new \PHPExcel();

        $PHPExcel->setActiveSheetIndex();

        $sheet = $PHPExcel->getActiveSheet();

        $sheet->getDefaultStyle()->getAlignment()->setWrapText(true);
        $sheet->getDefaultStyle()->getAlignment()->setVertical(\PHPExcel_Style_Alignment::VERTICAL_CENTER);

        $setBorderArray = array(
            'borders' => array(
                'allborders' => array(
                    'style' => \PHPExcel_Style_Border::BORDER_THIN
                )
            )
        );


        $sheet->getColumnDimension('A')->setWidth(10);
        $sheet->getColumnDimension('B')->setWidth(20);
        $sheet->getColumnDimension('C')->setWidth(20);
        $sheet->getColumnDimension('D')->setWidth(20);
        $sheet->getColumnDimension('E')->setWidth(20);

        $sheet->setCellValue('A3', 'No');
        $sheet->setCellValue('B3', 'Unit');
        $sheet->setCellValue('C3', 'Jenis Jabatan');
        $sheet->setCellValue('D3', 'Kelas Jabatan');
        $sheet->setCellValue('E3', 'Nilai Tpp');

        $PHPExcel->getActiveSheet()->setCellValue('A1', 'Data TunjanganUnitJenisJabatanKelas');

        $PHPExcel->getActiveSheet()->mergeCells('A1:E1');

        $sheet->getStyle('A1:E3')->getFont()->setBold(true);
        $sheet->getStyle('A1:E3')->getAlignment()->setHorizontal(\PHPExcel_Style_Alignment::HORIZONTAL_CENTER);

        $row = 3;
        $i=1;

        $searchModel = new TunjanganInstansiJenisJabatanKelasSearch();

        foreach($searchModel->getQuerySearch($params)->all() as $data){
            $row++;
            $sheet->setCellValue('A' . $row, $i);
            $sheet->setCellValue('B' . $row, @$data->instansi->nama);
            $sheet->setCellValue('C' . $row, @$data->getNamaJenisJabatan());
            $sheet->setCellValue('D' . $row, $data->kelas_jabatan);
            $sheet->setCellValue('E' . $row, $data->nilai_tpp);

            $i++;
        }

        $sheet->getStyle('A3:E' . $row)->getAlignment()->setHorizontal(\PHPExcel_Style_Alignment::HORIZONTAL_CENTER);
        $sheet->getStyle('D3:E' . $row)->getAlignment()->setHorizontal(\PHPExcel_Style_Alignment::HORIZONTAL_CENTER);
        $sheet->getStyle('E3:E' . $row)->getAlignment()->setHorizontal(\PHPExcel_Style_Alignment::HORIZONTAL_CENTER);


        $sheet->getStyle('C' . $row)->getAlignment()->setHorizontal(\PHPExcel_Style_Alignment::HORIZONTAL_CENTER);

        $sheet->getStyle('A3:E' . $row)->applyFromArray($setBorderArray);

        $path = 'exports/';
        $filename = time() . '_DataPenduduk.xlsx';
        $objWriter = \PHPExcel_IOFactory::createWriter($PHPExcel, 'Excel2007');
        $objWriter->save($path.$filename);
        return Yii::$app->getResponse()->redirect($path.$filename);
    }

}
