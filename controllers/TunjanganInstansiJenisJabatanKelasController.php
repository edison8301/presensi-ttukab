<?php

namespace app\controllers;

use Yii;

use yii\web\Controller;
use yii\web\NotFoundHttpException;
use yii\filters\VerbFilter;
use yii\filters\AccessControl;
use yii\helpers\Json;

use app\models\TunjanganInstansiJenisJabatanKelas;
use app\models\TunjanganInstansiJenisJabatanKelasSearch;
use app\models\User;

/**
 * TunjanganUnitJenisJabatanKelasController implements the CRUD actions for TunjanganUnitJenisJabatanKelas model.
